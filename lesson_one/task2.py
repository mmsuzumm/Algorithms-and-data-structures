a = 5
b = 6
print("%d = %s" % (a, bin(a)))
print("%d = %s" % (b, bin(b)))
print("%d & %d = %d (%s)" % (a, b, a & b, bin(a&b)))  # 101 и 100 тк "и", то 0 и 1 дают в результате 0
print("%d | %d = %d (%s)" % (a, b, a | b, bin(a|b)))  # 101 и 100 тк "или", то 0 и 1 дают в результате 1
print("%d << 2 = %d (%s)" % (a, a << 2, bin(b<<2)))  # Сдвиг вправо, 110+00 = 11000 =20
print("%d >> 2 = %d (%s)" % (a, a >> 2, bin(b>>2)))  # Сдвиг влево, убираем 2 знака остается 1